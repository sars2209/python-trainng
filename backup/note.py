# moddule模組
# 模組是一個獨立的檔案 能被程式載入使用
# 使用:先載入模組，再使用模組中的函式或變數
# 載入模組語法: "import 變數名稱" or "import 變數名稱 as 模組別名"
# 使用模組語法: "模組名稱或別名.定義在模組中的函式名稱(參數名稱)" "模組名稱或別名.變數名稱"
